#Write a Python program that uses a while loop to print the even numbers from 2 to 10

# Initialize the counter
number = 2

# Use a while loop to print even numbers from 2 to 10
while number <= 10:
# the while loop continues as long as the number is less than or equal to 10.
    print(number)
    number += 2  # Increment the counter by 2 to get the next even number
#Inside the loop, even numbers are printed, and the counter (number) is incremented by 2 to move to the next even number. 

#The loop continues until the condition is no longer true

# Print a message after the loop
print("End of program")
#The program then prints "End of program" after the loop.

