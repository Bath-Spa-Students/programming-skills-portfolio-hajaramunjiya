#Write a python program that stores fruit names in a list named fruits. Unpack the list into three variables x, y and z and then print the values of each variable   

# Store fruit names in a list named fruits
fruits = ["apple", "banana", "cherry"]
#In this program, we first store fruit names in the fruits list.

# Unpack the list into three variables
x, y, z = fruits
#we use unpacking to assign these values to variables x, y, and z

# Print the values of each variable with comments
print("Unpacked Fruit Names:")
print(f"x: {x}")  # Print the value of x (first fruit)
print(f"y: {y}")  # Print the value of y (second fruit)
print(f"z: {z}")  # Print the value of z (third fruit)
