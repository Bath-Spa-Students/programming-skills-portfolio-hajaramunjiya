# Assign a message to a variable
message = "Hello, this is the initial message."
#We assign the initial message to the variable message.

# Print the initial message
print(message)
#We print the initial message using print(message).

# Change the value of the variable to a new message
message = "Now, this is the new message."
#We then change the value of the message variable to a new message

# Print the new message
print(message)
#We then change the value of the message variable to a new message