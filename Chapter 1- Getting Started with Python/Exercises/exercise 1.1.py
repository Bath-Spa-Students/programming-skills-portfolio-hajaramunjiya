# Second verse
print("\t\tUp above the world so high,")  # Indented with two tabs
print("\t\tLike a diamond in the sky.")

# Third verse
print("Twinkle, twinkle, little star,")  # Back to the first-level indentation
print("\tHow I wonder what you are")  # No exclamation mark to match the original poem

