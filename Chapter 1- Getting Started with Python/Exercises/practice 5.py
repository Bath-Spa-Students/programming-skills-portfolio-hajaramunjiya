#Write four strings in different variables and print the output as one string 

# Define four strings in different variables
string1 = "Hello, "
string2 = "how are "
string3 = "you "
string4 = "today?"
#Four strings are defined in different variables (string1, string2, string3, and string4).

# Concatenate the strings into one string
result_string = string1 + string2 + string3 + string4
#The + operator is used to concatenate these strings into the result_string variable.

# Print the combined string
print(result_string)
#Finally, we print the result_string, which contains the combined string.

