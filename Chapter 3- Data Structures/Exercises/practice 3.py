#Assume names references a list. Write a for loop that displays each element of the list.

# Assume 'names' references a list
names = ['thahliya', 'asmina', 'aisha', 'naja', 'febi']
#we assume that names is a list of names

# Use a for loop to display each element in the list
#The for loop iterates through each element in the list, and the name variable represents each element one by one
for name in names:
    # 'name' will iterate through the elements of the 'names' list one by one
    print(name)
#We print each name within the loop.
