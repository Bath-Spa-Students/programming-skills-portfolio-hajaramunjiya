# List of motorcycle brands
motorcycle_brands = ['Honda', 'Yamaha', 'Kawasaki', 'Suzuki', 'Ducati']
#We have a list of motorcycle brands in the motorcycle_brands variable.

# Print statements about these motorcycles
for brand in motorcycle_brands:
    statement = f"I would like to own a {brand} motorcycle."
    print(statement)
#The for loop iterates through each brand in the list.

#Inside the loop, we create a statement using an f-string that incorporates the current brand, resulting in personalized statements for each motorcycle brand.

#We print each statement, which expresses the desire to own a motorcycle from each brand.