#Write a Python function that takes two numbers as arguments and returns their sum.

def add_numbers(a, b):
    """
    Function to add two numbers.

    Parameters:
    a (float or int): The first number.
    b (float or int): The second number.

    Returns:
    float or int: The sum of the two numbers.
    """
    return a + b
# the add_numbers function takes two parameters (a and b) and returns their sum using the + operator.

# Example usage:
num1 = 5
num2 = 8
result = add_numbers(num1, num2)
print(f"The sum of {num1} and {num2} is: {result}")
# the add_numbers function takes two parameters (a and b) and returns their sum using the + operator.
