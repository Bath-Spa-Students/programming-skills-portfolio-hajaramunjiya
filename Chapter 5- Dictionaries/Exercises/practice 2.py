#Write a Python program to iterate through the keys of a dictionary and print them.

#This program defines a dictionary (my_dict) and then uses a for loop to iterate through its keys using the keys() method
# Sample dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}

# Iterate through the keys and print them
print("Keys of the dictionary:")
for key in my_dict.keys():
    print(key)
    
