# Create a dictionary to store information about a person
person_info = {
    'first_name': 'hajara',  # First name of the person
    'last_name': 'ishaq',    # Last name of the person
    'age': 19,             # Age of the person
    'city': 'sharjah'     # City in which the person lives
}

# Print each piece of information stored in the dictionary with comments
print("First Name: " + person_info['first_name'])  # Print the first name
print("Last Name: " + person_info['last_name'])    # Print the last name
print("Age: " + str(person_info['age']))            # Print the age (convert to string for concatenation)
print("City: " + person_info['city'])              # Print the city
