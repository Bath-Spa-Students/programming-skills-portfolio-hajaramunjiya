#Write a Python program to iterate through the values of a dictionary and print them.

# Sample dictionary
my_dict = {'a': 1, 'b': 2, 'c': 3, 'd': 4, 'e': 5}
#we use a for loop to iterate through the values of the dictionary using the values() method.

# Iterate through the values and print them
print("Values of the dictionary:")
for value in my_dict.values():
    print(value)
    
